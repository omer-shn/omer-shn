package TechPractice02;

public class ifelse {
    public static void main(String[] args) {

        int x =1;
        int y=1;
        if(x++ < ++y) {
            System.out.println("hello");
        }else {
            System.out.println("welcome"); }
        System.out.println("Log :"+x+" :"+y);



    }
}
