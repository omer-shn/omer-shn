package TechQuestions2;

public class ques3WhileLoop {
    public static void main(String[] args) {


        //Type java code by using while loop.
        //Write a program that types first 30 consecutive odd integers.

        int od=1;
        int end=30;
        while(od<=end) {
            System.out.print(" "+od);
            od+=2;
            end++;}
    }
}
